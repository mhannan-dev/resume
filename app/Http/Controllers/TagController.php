    }

    /**
     * Remove the specified resource from storage.
     *
     * @param  \App\Tag  $tag
     * @return \Illuminate\Http\Response
     */
    public function destroy(Tag $tag)
    {
        $tag->delete();
        $tag->posts()->delete();
        if (request()->expectsJson()) {
            return response()->json([
                'message' => "Your category has been deleted.",
            ]);
        }

    }
